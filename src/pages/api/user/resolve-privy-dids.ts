async function handler(req: NextApiRequestWithUser, res: NextApiResponse) {
  if (req.method !== 'GET') {
    logger.warn(`Method not allowed: ${req.method}`);
    return res.status(405).json({ error: 'Method not allowed' });
  }

  if (PRIVY_DIDS.length === 0) {
    logger.warn('No privyDids configured in resolve-privy-dids endpoint');
    return res.status(400).json({
      error: 'No privyDids configured',
      message: 'Add at least one privyDid to PRIVY_DIDS in this endpoint file.',
    });
  }

  try {
    const users = await prisma.user.findMany({
      where: {
        privyDid: {
          in: PRIVY_DIDS,
        },
      },
      select: {
        id: true,
        privyDid: true,
      },
    });

    const userIdByPrivyDid = new Map(
      users.map((user) => [user.privyDid, user.id] as const),
    );

    const userIds = PRIVY_DIDS.map((privyDid) =>
      userIdByPrivyDid.get(privyDid),
    ).filter((userId): userId is string => Boolean(userId));

    logger.info(
      `Resolved ${userIds.length} userIds from ${PRIVY_DIDS.length} privyDids`,
    );

    return res.status(200).json({ userIds });
  } catch (error: any) {
    logger.error(
      `Error resolving userIds from privyDids: ${safeStringify(error)}`,
    );
    return res.status(500).json({
      error: error?.message || 'Internal server error',
      message: 'Error occurred while resolving user IDs from privyDids.',
    });
  }
}

export default withAuth(handler);
